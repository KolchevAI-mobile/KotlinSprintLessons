package org.example.firstLesson

fun main() {
    val length: Long = 40868600000L
    val age: Byte = 27
    val partOfDay: Float = 0.075f
    val seconds: Short = 6480
    val partOfYear: Double = 2.0547945205479453E-4
    val apogee: Int = 327000

    println("""
        length = $length
        age = $age
        partOfDay = $partOfDay
        seconds = $seconds
        partOfYear = $partOfYear
        apogee = $apogee
    """.trimIndent())
}